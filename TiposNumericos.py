"""
Tipo Númerico

"""



print("Tipos numéricos")

# Inteiros
num1 = 10
print(f"O número inteiro é: {num1}")

num2 = 34
print(f"O número inteiro é: {num2}")

soma = num1 + num2
print(f"A soma dos números inteiros é: {soma}") 

# Ponto flutuante
num3 = 3.14
print(f"O número de ponto flutuante é: {num3}")         
num4 = 2.718
print(f"O número de ponto flutuante é: {num4}")         
soma_float = num3 + num4
print(f"A soma dos números de ponto flutuante é: {soma_float}")         


# Multiplicação

mult1 =  34
mult2 = 17
resultado_mult = mult1 * mult2
print(f"O resultado da multiplicação é: {resultado_mult}")  


# Divisão
div1 = 10
div2 = 3
resultado_div = div1 / div2
print(f"O resultado da divisão é: {resultado_div}")

# Divisão inteira
div_int1 = 10
div_int2 = 3
resultado_div_int = div_int1 // div_int2
print(f"O resultado da divisão inteira é: {resultado_div_int}")

# Resto da divisão
resto_div1 = 10 
resto_div2 = 3
resultado_resto_div = resto_div1 % resto_div2   
print(f"O resultado do resto da divisão é: {resultado_resto_div}")


# Potenciação
base = 2        
expoente = 3
resultado_potencia = base ** expoente
print(f"O resultado da potenciação é: {resultado_potencia}")

# Subtração
sub1 = 10
sub2 = 5
resultado_sub = sub1 - sub2
print(f"O resultado da subtração é: {resultado_sub}")



# Tipo Float
num5 = 3.14
print(f"O número de ponto flutuante é: {num5}")
num6 = 2.718
print(f"O número de ponto flutuante é: {num6}")
soma_float = num5 + num6
print(f"A soma dos números de ponto flutuante é: {soma_float}")


var1 = 10.97    
print(f"O número de ponto flutuante é: {var1}")
var2 = 5.2
print(f"O número de ponto flutuante é: {var2}")
soma_var = var1 + var2
print(f"A soma dos números de ponto flutuante é: {soma_var}")

# Verificando o tipo da variável soma_var
print(type(soma_var))



# Numéros complexos
num_complexo1 = 2 + 3j
print(f"O número complexo é: {num_complexo1}")  
num_complexo2 = 1 - 4j
print(f"O número complexo é: {num_complexo2}")
soma_complexo = num_complexo1 + num_complexo2
print(f"A soma dos números complexos é: {soma_complexo}")
print(type(soma_complexo))
      



