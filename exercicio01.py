"""

2. Faça um programa que peça para o usuário digitar três valores inteiro e imprima a soma deles.

"""

num1 = int(input("Digite o primeiro número inteiro: "))
num2 = int(input("Digite o segundo número inteiro: "))      
num3 = int(input("Digite o terceiro número inteiro: "))

soma = num1 + num2 + num3
print(f"A soma dos números é: {soma}")  


"""
3. Faça um programa que recebe três valores e apresente a soma dos quadrados dos valores lidos

"""

num4 = int(input("Digite o primeiro número: "))
num5 = int(input("Digite o segundo número: "))      
num6 = int(input("Digite o terceiro número: "))
soma_quadrados = num4 ** 2 + num5 ** 2 + num6 ** 2
print(f"A soma dos quadrados dos números é: {soma_quadrados}")      

